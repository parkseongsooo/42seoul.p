#include<unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	swap;
	int	j;

	i = 0;
	j = 0;
	while (i < size)
	{
		while (j < (size - (i + 1)))
		{
			if (tab[j] > tab[j + 1])
			{
				swap = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = swap;
			}
			j++;
		}
		j = 0;
		i++;
	}
}
